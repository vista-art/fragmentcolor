    fn add_class(buf: &mut String, name: &str, doc: Option<String>) {
        buf.push_str(&format!("class {}:\n", name));
        if let Some(d) = doc {
            let d = sanitize_doc(&d);
            buf.push_str("    \"\"\"\n");
            for line in d.lines() {
                buf.push_str("    ");
                buf.push_str(line);
                buf.push('\n');
            }
            buf.push_str("    \"\"\"\n");
        }
    }

    fn doc_with_python_examples(rel: &str) -> Option<String> {
        let root = std::env::var("CARGO_MANIFEST_DIR").unwrap_or_else(|_| ".".into());
        let path = PathBuf::from(root).join(rel);
        let md = fs::read_to_string(path).ok()?;
        Some(render_doc_with_python(&md, rel))
    }

    fn render_doc_with_python(md: &str, label: &str) -> String {
        // Extract content before '## Example' and the first Rust example code block afterward.
        let mut pre = String::new();
        let mut in_example = false;
        let mut in_code = false;
        let mut code_lang = String::new();
        let mut rust_code = String::new();
        let mut captured = false;

        for line in md.lines() {
            let t = line.trim_start();
            if !in_example {
                if t.starts_with("## Example") {
                    in_example = true;
                    continue;
                }
                pre.push_str(line);
                pre.push('\n');
                continue;
            }
            if !in_code && t.starts_with("## ") {
                // end of example section
                break;
            }
            if !in_code && t.starts_with("```") {
                in_code = true;
                // try to capture only first rust or unlabeled block
                let lang = t.trim_start_matches("```").trim().to_ascii_lowercase();
                code_lang = if lang.is_empty() { "rust".into() } else { lang };
                continue;
            }
            if in_code && t.starts_with("```") {
                in_code = false;
                if !rust_code.is_empty() {
                    captured = true;
                }
                continue;
            }
            if in_code {
                if code_lang == "rust" && !captured {
                    rust_code.push_str(line);
                    rust_code.push('\n');
                }
            }
        }

        let mut out = String::new();
        let pre = sanitize_doc(&pre);
        out.push_str(&pre);

        if !rust_code.trim().is_empty() {
            // Convert to Python using the same converter as website/README flows
            let items = lines_to_items(&rust_code);
            let py_code = crate::convert::to_py(&items, label);
            out.push_str("\n\nExample (Python):\n\n");
            out.push_str("```python\n");
            out.push_str(py_code.trim_end());
            out.push_str("\n```\n");
        }

        out
    }

    fn lines_to_items(rust: &str) -> Vec<(String, bool)> {
        let mut items: Vec<(String, bool)> = Vec::new();
        for l in rust.lines() {
            let trimmed = l.trim_start();
            let indent_len = l.len() - trimmed.len();
            if trimmed.starts_with('#') {
                let after = trimmed.trim_start_matches('#').trim_start();
                let new_text = format!("{}{}", &l[..indent_len], after);
                items.push((new_text, true));
            } else {
                items.push((l.to_string(), false));
            }
        }
        items
    }

    fn sanitize_doc(s: &str) -> String {
        // Keep it simple: include raw markdown; triple quotes are unlikely, but escape if any
        s.replace("\"\"\"", "'''")
    }
