    fn find_object_dir(docs_root: &Path, dir_slug: &str) -> Option<PathBuf> {
        fn walk(dir: &Path, root: &Path, target: &str) -> Option<PathBuf> {
            if !dir.is_dir() {
                return None;
            }
            for entry in std::fs::read_dir(dir).ok()?.flatten() {
                let p = entry.path();
                if p.is_dir() {
                    if p.file_name().and_then(|s| s.to_str()) == Some(target) {
                        let md = p.join(format!("{}.md", target));
                        if md.exists() {
                            return Some(p);
                        }
                    }
                    if let Some(found) = walk(&p, root, target) {
                        return Some(found);
                    }
                }
            }
            None
        }
        walk(docs_root, docs_root, dir_slug)
    }

    fn doc_method_with_python(
        category_rel: &str,
        object_slug: &str,
        method_stem: &str,
    ) -> Option<String> {
        let root = std::env::var("CARGO_MANIFEST_DIR").unwrap_or_else(|_| ".".into());
        let docs_root = PathBuf::from(root).join("docs/api");
        let obj_dir = {
            let dir_slug = object_slug;
            // object dir is nested under category_rel
            // build path docs/api/<category_rel>/<dir_slug>
            let p = docs_root.join(category_rel).join(dir_slug);
            if p.join(format!("{}.md", dir_slug)).exists() {
                Some(p)
            } else {
                // fallback to recursive search
                find_object_dir(&docs_root, dir_slug)
            }
        }?;
        // Prefer Python-specific hidden docs when present
        let hidden_py = obj_dir
            .join("hidden")
            .join(format!("{}_py.md", method_stem));
        let normal = obj_dir.join(format!("{}.md", method_stem));
        if hidden_py.exists() {
            let md = fs::read_to_string(&hidden_py).ok()?;
            return Some(render_doc_with_python(&md, &hidden_py.to_string_lossy()));
        }
        if normal.exists() {
            let md = fs::read_to_string(&normal).ok()?;
            return Some(render_doc_with_python(&md, &normal.to_string_lossy()));
        }
        None
    }

    fn add_method_doc(buf: &mut String, category_rel: &str, object_slug: &str, method_stem: &str) {
        if let Some(doc) = doc_method_with_python(category_rel, object_slug, method_stem) {
            let d = sanitize_doc(&doc);
            buf.push_str("    \"\"\"\n");
            for line in d.lines() {
                buf.push_str("    ");
                buf.push_str(line);
                buf.push('\n');
            }
            buf.push_str("    \"\"\"\n");
        }
    }

